		<h1>Canon A1 i OpticFilm 8200i</h1>
		<p>
			Kiedyś nie było cyfry, negatywy to była codzienność. Z niego dalej "analogowo" przenosiło się
			obraz na papier. Dziś jeśli nie chcemy całkiem zrezygnować ze starej technologii (najczęściej
			z sentymentu) to możemy przerwać proces, np. na etapie negatywu i przenieść dalszy proces już
			na warstwę cyfrową. To taki kompromis i takie podejście wybrałem.
		</p>
		<p>
			Na co dzień używam aparatu Canon A1. Do skanowania negatywu - Plustek OpticFilm 8200i SE.
			Urządzenie jest dość powolne, ale za to uzyskamy dużą głębię koloru bo aż 48 bitową i
			rozdzielczość 720 dpi. To bardzo wysokie parametry, dużo przewyższające standardowe
			oczekiwania. Pewnie znajdziemy szybsze, tylko w tedy cena niestety bardzo mocno zaczyna
			rosnąć. Wcześniej testowałem starszy model OpticFilm podświetlany świetlówką, niestety miał
			uszkodzoną głowice CCD.
		</p>
		<p>
			Mając jeszcze kilka drobnych elementów w ciemni(typu korex, termometr, odczynniki itd.) można
			śmiało samodzielnie wywoływać negatyw. Ja skupiłem się na fotografii czarno-białej. Ułatwia mi
			to proces wywoływania, a jest to dla mnie wystarczające do wyrażenia swojego "podejścia do
			obrazu". Na szczęście kolor nie jest mi niezbędny do pracy twórczej. To inne podejście niż np.
			do fotografii okolicznościowej gdzie używam aparatu cyfrowego, bo w tej sytuacji nie jest
			ważna forma tylko zapis faktyczny świata. Używając A1 zmieniam sposób podejścia do tematu. Nie
			skupiam się na realnym świecie, można zapomnieć o wszystkich podstawowych technikach używanych
			do poprawnego zdjęcia (tego czego uczą w szkołach czy kursach fotograficznych), obserwuję
			formę obiektu, jej otoczenie, kontury, kontrast, a kolor no cóż jest drugorzędny bo tą
			informację gubimy i nie odzyskamy jej. Czy jest łatwiej? No nie, nie jest! Niestety nic łatwo
			nie przychodzi, fotografii czarno-białej też trzeba się nauczyć by dążyć do perfekcji, by
			uzyskać to co chcieliśmy.
		</p>
		<p>
			Takie podejście do zdjęcia można uzyskać aparatem cyfrowym, ba nawet smartfonem, są filtry do
			programów komputerowych symulujących szum negatywów. To jest racja, jednak jeśli chcemy poczuć
			fizycznie starą technologię, mieć na względzie jej ograniczenia i problemy (film, optyka itd)
			to śmiało polecam sięgnąć do analogu! Zdjęcia na urodzinach u cioci i tak wygodniej jest robić
			telefonem.
		</p>
