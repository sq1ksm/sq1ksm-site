			<h1>Sceny widowiska:</h1>
			<h2>Scena 1</h2>
			<p>
				Miejsce: stacja kolejowa w Krześnicy. Jest ciemno. Słychać z oddali pociąg. Pojawiają się na
				torach światła tego pociągu, ale pociąg nie wjeżdża na stację, słychać gwizd pociągu. Po
				torach biegnie spłoszony koń. Spomiędzy świateł wyłaniają się sylwetki ludzi, którzy niosą
				na plecach swoje tobołki, ciągną wózki, prowadzą kozę. Powoli peron stacyjny się zapełnia.
				Ludzie klną na Niemców, że tory rozebrali, złorzeczą maszyniście, kłócą się między sobą,
				rozlokowują się na całym peronie.<br />
				Grupa I – wyjmują z tobołów drzewiec, chorągiew kościelną i składają je; ksiądz wyciąga sutannę
				i ją ubiera, zakłada koloratkę, wyciąga krzyż i wiesza go na piersi. Każe swoim parafianom klęknąć
				i odmawia litanię. Pozostali na peronie cichną. Niektórzy klękają, inni są zajęci rozpakowywaniem
				tobołków. Gosposia wyciąga bochenek chleba, ksiądz go łamie i rozdaje między swoich parafian.
				Pyta innych czy chcą chleba bożego i wyspowiadać się przed dalszą tułaczką. Podchodzą matka z
				córką Walą (z grupy III). Dziękują księdzu, matka próbuje go w rękę pocałować, ale ten się broni.
				Intonuje pieśń kościelną (Boże coś Polskę). Jego parafianie bardzo żarliwie podejmują pieśń i
				patrzą na chorągiew, którą ksiądz podnosi do góry i obraca tak jakby wizerunek na niej miał oglądać
				tę nową ojczyznę. Młody kaleka płacze i mówi: - Patrz Matko Boska, oto nasz nowy dom, nowa ziemia
				i nowy los. Ksiądz każe się swoim parafianom ustawić do wymarszu. Schodzą na tory i z pieśnią
				na ustach dochodzą do drogi. Skręcają w lewo i znikają w ciemnościach.
			</p>
			<h2>Scena 2</h2>
			<p>
				Żołnierz krzyczy z oddalającą się grupą I, że to reakcjoniści, kułaki i ciemnota galicyjska.
				Aktywistki przytakują, jedna nawet zapisuje coś w kajecie. Niema kobieta szuka miejsca, by
				przywiązać swoją kozę. Przywiązuje ją do dyszla wózka ogrodowego, ale kobieta z małym
				dzieckiem na rękach każe jej ją odwiązać. Niema kobieta nie reaguje i idzie w krzaki za
				potrzebą. Mąż kobiety z dzieckiem z furią podbiega do kozy i kopie ją. Koza wystraszona z
				wózkiem zeskakuję na tory. Kobieta podnosi larum. Inni wyzywają męża, ratują kozę, kobieta z
				dzieckiem zbiera zawartość wózka, ktoś jej pomaga i nieoczekiwanie znajduje karabin.
				Rozpoczyna się kłótnia i posądzenia o donosicielstwo. Mąż tłumaczy się i dostaje w twarz od
				żołnierza. Żołnierz zabiera mu karabin. Kobieta lamentuje, dziecko płacze. Jeden z grajków
				wyciąga harmonię i zaczyna cicho śpiewać po ukraińsku (Oj, chmelu mij chmelu); do niego
				dołącza się drugi śpiewak i śpiewają dość głośno. Pozostali rozpalają ognisko. Kobiety
				przyrządzają prosty posiłek składający się z kaszy, słoniny i czarnej kawy. Niektórzy
				wyciągają resztki chleba, wrzucają rozdrobniony do menażek. Damy rozwijają swoje pakieciki
				ze skórkami od chleba, które jedzą nabożnie i zagryzają małymi plasterkami jabłka. Ktoś
				niecierpliwie pyta, kiedy krowa dojdzie. Matka Stepana odpowiada, że Stefan pewnie jeszcze
				doi ją, a potem musi ją jeszcze napaść, więc pewnie idą powoli. Zniecierpliwiony narzeka, że
				musi chleb zalać mlekiem i dać dziecku. Wala pyta się ojca, czy może pójść po Stepana, bo
				jemu pewnie ciężko krowę paść. Ojciec kategorycznie każe jej siedzieć na dupie, a nie
				obnosić ją po niemieckich krzakach. Mąż wyciąga butelkę bimbru, podchodzi do żołnierza i
				prosi, żeby napić się na zgodę, bo może sąsiadami zostaną. Inni też podtykają jakieś
				naczynia, żeby chociaż łyka dostać. Niema kobieta wychodzi z krzaków, rozgląda się za kozą i
				w złości podbiega do męża, wyciąga zza pazuchy nóż i chce go uderzyć, ale żołnierz chwyta ją
				za rękę i mówi, że koza uwiązana do płotu, a oni piją na zgodę, a nie za śmierć kozy. Niema
				kobieta wyciąga zza pazuchy kieliszek i każe sobie też nalać. Wypija głęboko się odchylając,
				a potem sięga za pazuchę i wyciąga stamtąd główkę obranej cebuli, którą zagryza wypity
				bimber. Pluje pod nogi żołnierzowi i coś bełkoce. Dziecko podbiega do niemej kobiety i
				ciągnie ją za spódnicę każąc jej pójść do jego mamy. Torami nadchodzi Stepan z krową i kanką
				mleka. Wala podbiega do niego i zalotnie proponuje, że mu bańkę potrzyma. Stepan próbuje
				krowę wciągnąć na peron. Jego matka popycha krowę, ale upada na torach. Dwie damy podbiegają
				do matki Stepana, podnoszą ją i układają na pierzynie wyciągniętej z tobołu. Stepan
				przywiązuje krowę do płotu i idzie z wiadrem po wodę za budynek stacyjny. Wala znika za nim.<br
				/>
				Damy biadolą, że starej kobiecie trzeba lekarza, bo strasznie dyszy i siły ją opuszczają. Grajkowie
				żartują, że trzeba jej kielicha dać, to wstanie i jeszcze potańczy. Ojciec Wali mówi, żeby paniusie
				się nie wtrącały, że wyglądają na takie co się znają na chorobach i że po co jadą na te niemieckie
				ziemie. Jedna z dam odpowiada, że będą nauczycielkami. Jedna uczyć będzie matematyki, a druga
				polskiego. Wszyscy wybuchają śmiechem, że to niby Niemców będzie tego polskiego uczyć, bo oni
				przecież są Polakami i tam koło Żytomierza chodzili do polskiej szkoły. Niektórzy to nawet cztery
				klasy mają. Tutaj będą na własnej ziemi pracować, bo władza ludowa im obiecała. Dom tylko potrzebny
				i uczciwy sąsiad. Żeby tylko nie był z Białorusi. Młode małżeństwo butnie pyta, czemu boją się
				tych z Białorusi, to Ukraińców trzeba się bać, bo mogą całą wieś wymordować. Aktywistki uspokajają
				ludzi mówiąc, że przecież wszyscy jesteśmy Polakami, że tamte strony trzeba wymazać z pamięci
				i poświęcić się budowie nowej Polski.<br />
				Zapada cisza. Wchodzi Stepan z wiadrem i Walą.
			</p>
			<h2>Scena 3</h2>
			<p>
				Słychać nadjeżdżający samochód bez świateł. Samochód jest radziecki, jeszcze z gwiazdą na
				masce i drzwiach. Zatrzymuje się na torach. Wychodzi z niego radziecki żołnierz (albo
				polski) i podchodzi do repatriantów. Wszyscy wstają i szukają papierów. Żołnierz wesoło
				pozdrawia ludzi i pyta, czy jest grupa, która ma jechać do Zicher, bo on może ich zabrać, że
				jeżeli mają broń, to niech oddadzą jemu, bo na miejscu będą tylko z tego kłopoty. Żołnierz
				polski oddaje broń. Grupa II najpierw wypytuje się, gdzie te Zicher są, czy są już tam jacyś
				repatrianci, skąd są. Żołnierz odpowiada, że różnie, trochę z Ukrainy, trochę z Białorusi, a
				trochę z Polski. A i Niemcy w nocy zjawiają się, żeby coś sobie ugotować. Młody mąż krzyczy,
				że po co oddał karabin, przecież trzeba się będzie przed Niemcami bronić. Żołnierz polski
				nakazuje swojej grupie zbierać się i ładować do wozu. Dziecko przyprowadza kozę dla niemej
				kobiety i pokazuje, że będą wyjeżdżać. Kobieta lamentuje, a dziecko tłumaczy sołdatowi, że
				koza nie chce wchodzić do samochodu. Żołnierz radziecki mówi, że trzeba kozę zastrzelić,
				mięso zjeść, bo nigdzie nie ma jedzenia i będą musieli przychodzić na posterunek, żeby swój
				chleb odebrać. Kobieta przestaje lamentować, bierze kozę na ręce i zanosi do samochodu.<br
				/>
				Grupa III dopytuje, czy i po nich ktoś przyjedzie. Sołdat odpowiada, że nie wie, że niech czekają
				do rana, to może jak milicja ich zaprowadzi. Każe być czujnym, bo faszyście jeszcze nie wszyscy
				zginęli. Grupa II wchodzi do samochodu. Ktoś w samochodzie śpiewa przedwojenną piosenkę polską
				(Ach jak przyjemnie). Dołączają się aktywistki. Samochód odjeżdża w kierunku Sarbinowa.
			</p>
			<h2>Scena 4</h2>
			<p>
				Stepan siada przy matce. Zapala naftową latarkę i podaje menażkę z mlekiem. Pyta innych czy
				chcą mleka. Rozdaje mleko. Bogdan i Prakseda zaczynają pytać, czy aby ktoś wie, że oni
				przyjechali, że nie mają przydziału na wieś, tylko do miasta, ale wolą ze znajomymi, bo się
				Niemców boją. Matka Wali wątpi czy ich ktoś zabierze, więc proponuje ruszyć stąd. Niech
				grajkowie pójdą przodem z pochodniami i znajdą jakiś dom, gdzie mogliby przenocować, a rano
				się zobaczy. Grajkowie się targują, że za durno nie pójdą, bo albo ich Niemcy ustrzelą z
				lasu, albo ruscy zabiją jak szabrowników. Za litra mogą spróbować. Ojciec Wali wyraża chęć
				pójścia z grajkami, ale przedtem wypiją na odwagę. Wala szykuje zagrychę – słonina i cebula,
				kartofel upieczony w ognisku. Grajkowie zaczynają intonować pieśń ukraińską (Horyła sosna).
				Damy nieśmiało protestują, że boją się zostać na stacji, bo jak Niemcy wrócą…, że zostają
				prawie same kobiety, więc może babcię na wózek i razem pójdą poszukać schronienia na noc.
				Stepan mówi, że zostanie z matką na stacji, a jak rano ktoś przyjedzie, to powie, gdzie
				wszyscy poszli. Wala najpierw protestuje, wreszcie z płaczem oświadcza, że zostaje ze
				Stepanem i krową. Ojciec Wali wpada w szał dobiega do niej i próbuje ją uderzyć, ale drogę
				zastępuje mu Stepan, który mówi, że w nowym domu, będą nowe porządki i żaden starzyk nie
				będzie bił kobiet, a tym bardziej decydował, kto co ma robić, że skończyła się epoka
				ruskiego poniewierania ludzi. Matka Wali próbuje odepchnąć swojego męża krzycząc, że na
				wojnie się trzeba było bić, a nie teraz do swoich skakać, tym bardziej, że Stepan to jedyny
				kawaler na tym niemieckim pustkowiu. I niech ich Bóg prowadzi. Grajkowie krzyczą, że trzeba
				będzie wesele zrobić, nawet na stacji.<br />
				Bogdan zapala pochodnie, grajkowie zapalają latarki naftowe, pakują starą kobietę na wózek, który
				ciągnie Stepan, a Wala prowadzi krowę i idą w kierunku Krześnicy. Kobiety niosą latarki. Grajkowie
				na przedzie drą się śpiewając Horyła sosna. (widzowie z pochodniami idą za Grupą III)
			</p>
			<h2>Scena 5</h2>
			<p>
				Mijają tablicę z nazwą miejscowości Wilkersdorf. Zatrzymują się i nasłuchują. Dama zaczyna
				chlipać pytając czy aby to nie cmentarz, bo tak cicho, że nawet psy nie ujadają. Bogdan prze
				naprzód twierdząc, że i na cmentarzu można mieszkać, byle daleko od sowietów i bandytów.
				Wszyscy gorączkowo podążają za nim.<br />
				Zachodzą na podwórze w centrum wsi. Pukają do rozwalających się drzwi. Wchodzą powoli (za nimi
				wchodzą widzowie), rozglądają się po domu, a ojciec Wali mówi, że tutaj wszystkie rodziny mogą
				się pomieścić. Opuszczają dom, aby przynieść swój dobytek, a kiedy przychodzą pod drzwi Bogdan
				z Praksedą zatrzaskują im je przed nosem krzycząc, że to będzie ich dom i że mają iść sobie szukać
				nowego, swojego. Gromadzą się na podwórku, wychodzą na skrzyżowanie i zastanawiają czy szukać
				razem, czy osobno. Ruszają osobno w różne strony. Zostają grajkowie, którzy śpiewają Pada deszczyk
				pada. Widzowie zostają na skrzyżowaniu. Aktorzy zjawiają się na skrzyżowaniu z różnych stron
				i kłaniają. Żołnierz zaprasza wszystkich na kolację do świetlicy.
			</p>
			<h2>PIEŚŃ</h2>
