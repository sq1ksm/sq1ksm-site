		<p>
			<br />
			Śmiało mogę powiedzieć, że jestem pionierem kart ekologicznych. Nie wszystkie karty chcemy zachować
			"na wieczność", część z nich nie wymaga dodawania do nich plastiku, czy innych substancji powiększających
			ich trwałość. Drukując np. samodzielnie karty możemy ograniczyć ich ilość do niezbędnego minimum
			(drukarnia drukuje najczęściej wielokrotności 1000 szt.) Nieczęsto wysyłamy karty kolegom z Polski,
			albo jako już drugą kartę przypominającą. Warto pomyśleć w tym monecie o przyrodzie!
		</p>
		<p>
			Karty zawierające logo <b>Ecology QSL Cards</b> gwarantują nietoksyczność:<br />
		</p>
		<ul>
			<li>użytego papieru (nie bielony chemicznie, najlepiej z recyklingu);</li>
			<li>nadruku (obojętne dla środowiska barwniki i tusze np. sadza/grafit);</li>
			<li>
				nie zawierają w składzie folii (tworzyw sztucznych, lakierów, powłok laminujących) i innych
				"niebiodegradowalnych" elementów.
			</li>
		</ul>

