		<h1>Smolnica, praca z młodzieżą</h1>
		<p>
			Prelekcja 24 02 2010 Łączności Cyfrowe w Krótkofalarstwie.<br />
			Przebieg: <br />
			Wstęp -Henryk SP3FYJ<br />
			Ogólnie o krótkofalarstwie -Stefan SP1JJY<br />
			Łączności cyfrowe -Mieczysław SP3CMX<br />
			Praca na PSK31 i zajęcia praktyczne z łączności -Sławek (piszący te słowa) SQ1KSM.<br />
		</p>
		<p>
			Całość przebiegało zgodnie z założeniami w czasie 2h. Przedstawiliśmy młodzieży nasze hobby
			-Krótkofalarstwo. Omówiona została historia radioamatorstwa, początki w Polsce założenia PZK.
			Opowiedzieliśmy o nowoczesnych środkach przekazu łącznościach cyfrowych, poprzez satelity,
			digivoice, ale nie zapomnieliśmy o tradycyjnej łączności FM, SSB czy CW oraz coraz rzadziej
			organizowanych tzw. "łowach na lisa". Po części teoretycznej pod okiem licencjonowanego
			krótkofalowca kilku młodych ludzi zrobiło swoje pierwsze łączności na BPSK31 na częstotliwości
			14 MHZ z radioamatorami z Europy i zostało im wręczone dyplomy- "MOJE PIERWSZE QSO".<br />
		</p>
