		<h1>2024 Amateur Radio Kids Day w Krześnicy</h1>
		<p>
			Amateur Radio Kids Day czyli Krótkofalarski Dzień Dzieci i Młodzieży obchodzony jest zawsze
			dwukrotnie w ciągu roku. 14 Czerwca 2024 pod znakiem naszego klubu SP1PNW Weronika SQ1KSL i
			Sławek SQ1KSM zorganizowali pracę stacji SP1PNW w świetlicy w miejscowości Krześnica (~10km od
			Dębna). Pandemia i inne trudności spowodowały dużą przerwę od ostatniego tego typu wydarzenia.
			W tym dniu wszystkie dzieci mogły przeprowadzić łączności w paśmie 2 i 40 m.<br />
		</p>
		<p>
			Na pomoc przyjechał Sebastian SP3W wraz z żoną i synem. Rozwiesiliśmy anteną FD4 i
			podłączyliśmy ją do TRX FT450, ale niestety propagacja nas nie rozpieszczała. Weronika
			opowiedziała o naszym hobby, Sebastian o pracy terenowej oraz pomagał młodzieży w pierwszych
			łącznościach. Odważni, którzy przeprowadzili łączność otrzymali <b>pamiątkowe dyplomy</b>.
			Gratulujemy dzieciom ich pierwszych łączności na pasmach KF!<br />
			Wszystkim naszym korespondentom serdecznie dziękujemy za okazane ciepło i cierpliwość.<br />
		</p>
