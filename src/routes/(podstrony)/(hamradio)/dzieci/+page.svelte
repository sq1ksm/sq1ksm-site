		<h1>2015 Amateur Radio Kids Day w Krześnicy</h1>
		<a href="/zdjecia/dzieci/dyplom_2015.webp" rel="lytebox[vacation]"
			><img
				height="150"
				src="/zdjecia/dzieci/dyplom_2015_m.webp"
				alt=" zdjęcie"
				title="Dyplom ARKD 2015"
				style="margin:10px 0px 0px 10px;float:right"
			/></a
		>
		<p>
			Od 7.06.2015 do 30.06.2015 trwa krótkofalarska akcja dyplomowa „Tytus, Romek i A’Tomek”,
			której podstawowym celem jest uczczenie urodzin Henryka Jerzego Chmielewskiego, polskiego
			grafika, rysownika, znanego jako Papcio Chmiel, twórcy serii komiksu pt. „Tytus, Romek i
			A'Tomek”, ur. 7 czerwca 1923 roku. Akcja "Tytus Romek i A'Tomek" łączy się pięknie z
			Krótkofalarskim Dniem Dziecka, który miał miejsce 21 czerwca. Z tej właśnie okazji w świetlicy
			w Krześnicy rozlokowano stację HF0TYTUS. Dzięki pomocy kolegów biorących udział w akcji
			rozłożono anteny, zainstalowano radiostację i po krótkim teoretycznym wstępie, swoich sił w
			przeprowadzeniu łączności drogą radiową mogły spróbować dzieci. Okazały się być bardzo pilnymi
			uczniami i przy niewielkiej pomocy, pięknie i fachowo prowadziły łączności, a korespondenci
			chwalili je za odwagę i staranność życząc powodzenia w rozwijaniu krótkofalarskiej pasji.<br
			/>
			W tym roku przy radiostacji prym wiodły dziewczyny. Szczególne gratulacje należą się Zuzi, Nikoli,
			Angelice, Roksanie, Martynce, Oli i Darii. Chłopcy pilnie obserwowali, mając więc niezłe przygotowanie
			teoretyczne, w kolejnej akcji to oni będą z pewnością przy mikrofonie.<br />
			Ogółem udało się dziewczynom przeprowadzić 150 łączności, głównie ze stacjami z całej Polski, ale
			na wywołanie odpowiedziało również wiele stacji z Niemiec, oraz stacje z Węgier, Czech, Włoch,
			Danii czy Estonii. Na pamiątkę dziewczyny otrzymają dyplomy, oraz karty QSL potwierdzające przeprowadzone
			łączności.<br />
			Był to drugi Krótkofalarski Dzień Dziecka w świetlicy w Krześnicy, z pewnością nie ostatni. Krótkofalarstwo
			to nie tylko frajda z rozmowy z osobą po drugiej stronie mikrofonu, ale także pobudzająca ciekawość
			świata pasja zachęcająca do zaglądania na mapę świata, nauki języków, oraz aktywności na wielu
			innych polach, bo krótkofalarstwo to bardzo szeroka dziedzina.
		</p>
