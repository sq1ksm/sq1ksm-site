		<p>
			Później rozstawiłem drugą stację QRPP (0.25W) nieopodal w parku i zrobiłem pokazową łączność
			ze swojego prywatnego znaku z dziećmi w świetlicy. Radości podczas wszystkich łączności było
			wiele, dla nich to zawsze jest jakieś przeżycie, trema którą trzeba przełamać by porozmawiać z
			dorosłym operatorem nie koniecznie mieszkającym w Polsce. Ze smutkiem piszę, ale niestety
			staje się to regułą że to dziewczyny łapią za mikrofony, a chłopaki ukradkiem patrzą i trzeba
			ich mocno namawiać...<br />
			Dziękuję również wszystkim którzy nas odwiedzili w tym dniu: Markowi SP3GVX i Tadeuszowi SP1WWS
			i nas wspierali (SP3SBY, SQ1NXN, SQ1NXZ, SP1FMW).
