		<h1>2016 Amateur Radio Kids Day w Krześnicy</h1>

		<img
			height="150"
			width="150"
			src="/zdjecia/dzieci/logo.webp"
			alt=" zdjęcie"
			title="Logo ARKD"
			style="margin:10px 10px 0px 0px;float:left"
		/>
		<p>
			Już po raz trzeci w Krześnicy zorganizowaliśmy (Weronika SQ1KSL - opiekun Świetlicy i piszący
			te słowa Sławek SQ1KSM) Krótkofalarski Dzień Dzieci i Młodzieży. W tym roku pod patronatem
			klubu SP1PNW młodzież i dzieci mogły z tymczasowej stacji próbować swoich sił w łącznościach
			krótkofalarskich. Pracowaliśmy w paśmie 80 m (okolice 3.7MHz), 40m oraz 2m na częstotliwość
			145.550 MHz. Wszystkim kolegom krótkofalowcom dziękujemy za wyrozumiałość i cierpliwość, którą
			musieli wykazać by ułatwić im pierwsze kontakty radiowe na pasmach.<br />
			Dzień dziecka rozpoczęliśmy około godziny 14:00. Kilka minut zajęło mi i Markowi (SP3GVX) rozłożenie
			anteny. Z dachu świetlicy rozłożyliśmy ramiona anteny FD4, niestety zabrakło już czasu na większą
			antenę UKF, i na tym paśmie rozmawialiśmy tylko lokalnie z anten TRX przenośnych. Dzieci już obserwowały
			nas od początku pomimo, że oficjalne rozpoczęcie było 2 godziny później, tak sobie zaplanowaliśmy,
			ale jak odmówić zniecierpliwionym dzieciom.... no i praktycznie zaczęliśmy godzinę wcześniej -
			ledwo udało się rozłożyć sprzęt. Do pracy wykorzystaliśmy prywatny sprzęt z naszego QTH w Krześnicy.
			Rozstawiliśmy jako główne stanowisko FT-450 ze skrzynką i zasilaczem podłączonych do wspomnianej
			anteny. Prawie wszystko nam sprzyjało, jedynie propagacja była niełaskawa. Taką samą sytuację mieliśmy
			2 lata temu, w poprzednim roku mieliśmy więcej szczęścia. Pomimo tego zainteresowanie radiem było
			wysokie. Nie wszyscy odważyli się chwycić mikrofon, ale te które złapały nie chciały się oderwać...
		</p>
		<a href="/zdjecia/dzieci/p2016.webp" rel="lytebox[vacation]"
