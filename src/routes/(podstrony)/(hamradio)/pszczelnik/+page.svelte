		<p>
			W dniach 13–14 lipca 2013 r. w Pszczelniku koło Myśliborza odbył się Zlot krótkofalowców
			"PSZCZELNIK 2013". Myśliborski Klub SP1PMY dla upamiętnienia 80. rocznicy przelotu przez
			Atlantyk litewskich lotników Dariusa i Girenasa, którzy tragicznie zginęli w lesie pod
			Pszczelnikiem. Zlot obfitował w wiele ciekawych atrakcji dla krótkofalowców i nie tylko.<br />
			Były wręczane dyplomy, losowanie upominków, wycieczka do muzeum, spływ kajakowy oraz jako ciekawostka
			certyfikat QLF, tj. dla osób, które posiadają umiejętność nadawania telegrafii lewą nogą.
		</p>
		<p>
			Oczywiście dzięki licznie przybyłym krótkofalowcom i sympatykom spotkanie było kolejnym
			momentem integracji środowiska. Drugiego dnia pod pomnikiem Dariusa i Girenasa odbyły się
			uroczystości upamiętniające tragiczną rocznicę, w których wzięła udział prezydent Litwy, pani
			Dalia Grybauskaite. Impreza bardzo udana – za rok wrócimy do Pszczelnika.
		</p>
