		<h2>2012 rok</h2>
		<p>
			W dniach 1–30 lipca 2012 roku pracowaliśmy pod okolicznościowym znakiem SN0LOT dla uczczenia
			wyczynu dwóch litewskich pilotów: Steponasa Dariusa i Stasysa Girenasa, którzy 15 lipca 1933
			roku małym samolotem "Lituanica" wylecieli z Nowego Jorku do Kowna. Po pokonaniu 6411 km, z
			dotychczas niewyjaśnionych do końca przyczyn, 17 lipca 1933 roku samolot się rozbił w pobliżu
			miejscowości Pszczelnik koło Myśliborza. Lotnicy zginęli. Do Kowna zostało im 650 km. 8 lipca
			2012 roku przed pomnikiem poległych lotników odbyły się oficjalne uroczystości ku ich czci.
		</p>
		<p>
			W tym dniu tradycyjnie zainstalowano obok pomnika radiostację krótkofalową, która nadawała pod
			znakiem SN0LOT, po raz pierwszy pod znakiem klubowym SP1PMY, i jak co roku pieczę nad
			wszystkim trzymał Stefan SP1JJY. Nasz klub wspomógł sprzętem na KF, agregatem prądotwórczym,
			no i swoimi osobami, hihi.
		</p>
