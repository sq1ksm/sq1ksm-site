		<h2>Czy potrzebne są dodatkowe filtry w FT817/818?</h2>
		<p>
			<left>
				FT817/FT818 to najmniejszy transceiver pozwalający na pracę w pasmach KF a także UKF emisją
				SSB, CW, AM i FM. Waży tylko 1 kg a jego wymiary to 3,8 x 13,5 x 16,5 cm. Co prawda tak mała
				płyta czołowa nie pozwala na umieszczenie dużej liczby przycisków, jednak te
				najpotrzebniejsze są dostępne, a rzadziej używane funkcje można uaktywnić z przejrzystego
				menu. Wersja ND posiada zmodyfikowany stopień mocy w starszej wersji, niektórzy użytkownicy
				podnosili moc do 10-15W i końcówki mocy padały<br />
				przy braku dopasowania anteny jak muchy) oraz trzy a nie <br />
				dwa kolory podświetlania wyświetlacza.
				<br /><br />
				Zalety:
			</left>
		</p>
		<ul>
			<li>wyjątkowo mała, zwarta konstrukcja;</li>
			<li>2 złącza antenowe dowolnie konfigurowane (nawet jako TX i RX w SPLICIE);</li>
			<li>200 komórek pamięci z możliwością ich opisania;</li>
			<li>możliwość zainstalowania dodatkowego filtra (np. dla CW SSB);</li>
			<li>miejsce na wewnętrzny akumulator lub pakiet baterii;</li>
			<li>-możliwość współpracy urządzenia z komputerem -CAT.</li>
			<li></li>
		</ul>
		Wady:
		<ul>
			<li>
				niefortunnie umieszczony przycisk „F” – mógłby być zamieniony miejscami z przyciskiem „LOCK”
				(Ale idzie się do tego przyzwyczaić);
			</li>
			<li>słaba jakość modulacji AM (niestety modulacja jest na niskim poziomie);</li>
			<li>brak kompresora dynamiki (można użyć mikrofon z kompresorem dynamiki);</li>
			<li>brak możliwości odbioru w paśmie 150 – 180 MHz (można użyć konwerterów).</li>
		</ul>

		Reasumując: FT817ND (FT818) jest świetnym urządzeniem, pomimo tych kilku wad. Większość
		użytkowników FT-817 (FT818) traktuje go bardzo "uczuciowo". Radio albo się pokocha albo się
		znienawidzi.
		<p></p>
