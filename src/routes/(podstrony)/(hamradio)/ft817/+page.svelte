		<h2>Zestaw "polowy" do pracy QRP</h2>
		Poniżej pokazałem, że można zmieścić w standardowej małej walizce plastikowej cały zestaw do pracy
		QRP. W zestawie znajduje się:
		<ul>
			<li>FT817ND z mikrofonem i terminatorem 50 Omowym, radio zakończone "śrubką uziemiającą".</li>
			<li>
				Skrzynka antenowa Elecraft T1 (urządzenie max 20W o niewiarygodnie niskim poborze mocy -
				tylko podczas strojenia!)
			</li>
			<li>Kabel mikroprocesorowy łączący skrzynkę antenową z radiem.</li>
			<li>Akumulator TURNIGY 2.2Ah LI-Po</li>
			<li>Balun z przełącznikiem 1:1/1:4</li>
			<li>Konektor BNC-2XBanan</li>
			<li>2X 5m fidera antenowego silikonowego srebrzonego.</li>
			<li>7mb anteny na zwijaku</li>
			<li>przyłącze BNC/SMA, złączki zasilania.</li>
			<li>Ołówek i kartka- do notatek.</li>
		</ul>
